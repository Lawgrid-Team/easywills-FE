// Helper functions for address data
export function getCountries(): { value: string; viewValue: string }[] {
    return ADDRESS_DATA.map((country) => ({
        value: country.country.toLowerCase().replace(/\s+/g, '-'),
        viewValue: country.country,
    }));
}

export function getStatesForCountry(
    countryValue: string
): { value: string; viewValue: string }[] {
    const countryData = ADDRESS_DATA.find(
        (c) => c.country.toLowerCase().replace(/\s+/g, '-') === countryValue
    );
    return countryData
        ? countryData.states.map((state) => ({
              value: state.value,
              viewValue: state.name,
          }))
        : [];
}

export function getCitiesForState(
    countryValue: string,
    stateValue: string
): { value: string; viewValue: string }[] {
    const countryData = ADDRESS_DATA.find(
        (c) => c.country.toLowerCase().replace(/\s+/g, '-') === countryValue
    );
    if (!countryData) return [];

    const stateData = countryData.states.find((s) => s.value === stateValue);
    return stateData
        ? stateData.cities.map((city) => ({
              value: city.value,
              viewValue: city.name,
          }))
        : [];
}
